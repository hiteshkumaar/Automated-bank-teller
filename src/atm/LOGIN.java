    private void LoginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginBtnMouseClicked
       if(UnameTb.getText().isEmpty() || PasswordTb.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(this,"Enter Acc number and PIN");
       }else{
        String Query = "select * from account where AccNum='"+UnameTb.getText()+"' and PIN="+PasswordTb.getText()+"";
        try{
             Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atmdb","root","");
             St = Con.createStatement();
             Rs = St.executeQuery(Query);
             if(Rs.next())
             {
                 new MainMenu(Rs.getInt(1)).setVisible(true);
                 this.dispose();
             }else
             {
                 JOptionPane.showMessageDialog(this,"Wrong Acc number or PIN");
             }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
      } 
    }//GEN-LAST:event_LoginBtnMouseClicked

